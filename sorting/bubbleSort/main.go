package main

import "fmt"

func bubbleSorter(arr []int) {
	// Получаем длину массива и сохраняем в переменную l
	l := len(arr)

	// Внешний цикл, который выполняется (l-1) раз
	// Каждая итерация "всплывает" один элемент в правильную позицию
	for i := 0; i < l - 1; i++ {

		// Внутренний цикл, который сравнивает соседние элементы
		// С каждой итерацией внешнего цикла уменьшаем количество сравнений,
		// так как последние i элементов уже отсортированы
		for j := 0; j < l - i - 1; j++ {

			if arr[j] > arr[j + 1] {
				// Обмен значений с использованием множественного присваивания в Go
				// Временная переменная не требуется
				arr[j], arr[j + 1] = arr[j + 1], arr[j]
			}
		}
	}
}

func main() {
	arr := []int{163, 300, 184, 191, 174}
	bubbleSorter(arr)
	fmt.Println(arr)
}
